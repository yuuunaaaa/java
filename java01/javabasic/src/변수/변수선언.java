package 변수;

public class 변수선언 {
	public static void main(String[] args) {
		int num = 100;
		double num2 = 22.2;
		char c = '한'; // 글자 1글자
		boolean b = true;
		num = 200;
		System.out.println("정수값은 " + num);
		System.out.println("실수값은  " + num2);
		System.out.println("문자1개 값은 " + c);
		System.out.println("부울렌 값은 " + b);
	}
}
