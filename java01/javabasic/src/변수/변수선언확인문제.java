package 변수;

public class 변수선언확인문제 {
	public static void main(String[] args) {
		int age = 24;
		double height = 159.8;
		char firstName = '현'; // 글자 1글자
		String name = "현유나";
		boolean women = true;
		System.out.println("나의 나이는 " + age);
		System.out.println("내 키는  " + height);
		System.out.println("내 성은 " + firstName);
		System.out.println("성별이 여자? " + women);
		System.out.println("나의 이름은 " + name);
	}
}
